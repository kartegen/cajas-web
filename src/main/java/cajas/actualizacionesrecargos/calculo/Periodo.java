package cajas.actualizacionesrecargos.calculo;

public class Periodo {
	private int ejercicioInicial;
	private int mesInicial;
	private int ejercicioFinal;
	private int mesFinal;

	public int getEjercicioInicial() {
		return ejercicioInicial;
	}

	public void setEjercicioInicial(int ejercicioInicial) {
		this.ejercicioInicial = ejercicioInicial;
	}

	public int getMesInicial() {
		return mesInicial;
	}

	public void setMesInicial(int mesInicial) {
		this.mesInicial = mesInicial;
	}

	public int getEjercicioFinal() {
		return ejercicioFinal;
	}

	public void setEjercicioFinal(int ejercicioFinal) {
		this.ejercicioFinal = ejercicioFinal;
	}

	public int getMesFinal() {
		return mesFinal;
	}

	public void setMesFinal(int mesFinal) {
		this.mesFinal = mesFinal;
	}

}
